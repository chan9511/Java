package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); // 5는 3보다 크다
        System.out.println(5 >= 3); // 5는 3보다 같거나 크다
        System.out.println(5 >= 5); // 5는 5보다 같거나 크다
        System.out.println(5 >= 7); // 5는 7보다 같거나 크다

        System.out.println(5 == 5);
        System.out.println(5 == 3);
        System.out.println(5 != 3);
    }

}
